void main() {
  int n1 = 4;
  int n2 = 8;
  
  int soma = n1 + n2;
  print('$n1 + $n2 = $soma');
  int subtracao = n2 - n1;
  print('$n2 - $n1 = $subtracao');
  int multi = n1 * n2;
  print('$n1 x $n2 = $multi');
  double div = n1 / n2;
  print('$n1 / $n2 = $div');
  int resto = n1 % n2;
  print('$n1 % $n2 = $resto');
}
